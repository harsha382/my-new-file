package arraylist;

public class SuperHeros {
	String name;
	String weapon;
	public SuperHeros(String name, String weapon) {
		super();
		this.name = name;
		this.weapon = weapon;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWeapon() {
		return weapon;
	}
	public void setWeapon(String weapon) {
		this.weapon = weapon;
	}
}
