package practice1;

public class Superheros {
	private String name;
	private String weapon;

	public Superheros(String name, String weapon) {

		this.name = name;
		this.weapon = weapon;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWeapon() {
		return weapon;
	}

	public void setWeapon(String weapon) {
		this.weapon = weapon;
	}

}
